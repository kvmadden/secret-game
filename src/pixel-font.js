const FONTS = {
  small:  { glyphs: SMALL,  w: SMALL_W,  h: SMALL_H  },
  medium: { glyphs: MEDIUM, w: MEDIUM_W, h: MEDIUM_H  },
  large:  { glyphs: LARGE,  w: LARGE_W,  h: LARGE_H   },
};

// ---------------------------------------------------------------------------
// RENDER CACHE
// ---------------------------------------------------------------------------
const _cache = new Map();
const MAX_CACHE = 512;

function cacheKey(text, opts) {
  return `${text}|${opts.size}|${opts.color}|${opts.shadow}|${opts.shadowColor}|${opts.align}|${opts.maxWidth}|${opts.lineHeight}|${Array.isArray(opts.color) ? opts.color.join(',') : ''}`;
}

function cacheGet(key) {
  return _cache.get(key) || null;
}

function cacheSet(key, canvas) {
  if (_cache.size >= MAX_CACHE) {
    // Evict oldest entry
    const first = _cache.keys().next().value;
    _cache.delete(first);
  }
  _cache.set(key, canvas);
}

// ---------------------------------------------------------------------------
// HELPERS
// ---------------------------------------------------------------------------

/** Resolve the glyph for a character. Lowercase maps to uppercase. */
function getGlyph(glyphs, ch) {
  if (glyphs[ch]) return glyphs[ch];
  const upper = ch.toUpperCase();
  if (glyphs[upper]) return glyphs[upper];
  return null; // unknown char rendered as blank
}

/** Default options merged with user-supplied ones. */
function defaults(options) {
  const o = options || {};
  return {
    size: o.size || 'medium',
    color: o.color || '#FFFFFF',
    shadow: o.shadow !== undefined ? o.shadow : false,
    shadowColor: o.shadowColor || '#000000',
    align: o.align || 'left',
    maxWidth: o.maxWidth || 0,
    lineHeight: o.lineHeight || 0,
  };
}

/** Split text into lines that fit within maxWidth (word wrap). */
function wordWrap(text, maxWidth, font) {
  if (!maxWidth || maxWidth <= 0) return [text];
  const charW = font.w + 1; // glyph width + 1px kerning
  const words = text.split(' ');
  const lines = [];
  let currentLine = '';

  for (let i = 0; i < words.length; i++) {
    const word = words[i];
    const testLine = currentLine.length === 0 ? word : currentLine + ' ' + word;
    const testWidth = testLine.length * charW - 1; // minus trailing kern
    if (testWidth > maxWidth && currentLine.length > 0) {
      lines.push(currentLine);
      currentLine = word;
    } else {
      currentLine = testLine;
    }
  }
  if (currentLine.length > 0) lines.push(currentLine);
  return lines;
}

/** Measure width of a single line of text in pixels. */
function measureLine(text, font) {
  if (text.length === 0) return 0;
  return text.length * (font.w + 1) - 1; // 1px kerning between chars, no trailing
}

/** Create an offscreen canvas (works in browser). */
function createCanvas(w, h) {
  if (typeof OffscreenCanvas !== 'undefined') {
    return new OffscreenCanvas(w, h);
  }
  const c = document.createElement('canvas');
  c.width = w;
  c.height = h;
  return c;
}

/** Draw a single glyph onto a context using imageData for pixel-perfect rendering. */
function drawGlyph(ctx, glyph, x, y, w, h, color) {
  if (!glyph) return;
  for (let row = 0; row < h; row++) {
    const bits = glyph[row] || 0;
    for (let col = 0; col < w; col++) {
      // MSB-first: check bit (w-1-col)
      if (bits & (1 << (w - 1 - col))) {
        ctx.fillRect(x + col, y + row, 1, 1);
      }
    }
  }
}

/** Parse a hex color to {r,g,b}. */
function parseHex(hex) {
  let h = hex.replace('#', '');
  if (h.length === 3) h = h[0]+h[0]+h[1]+h[1]+h[2]+h[2];
  return {
    r: parseInt(h.substring(0, 2), 16),
    g: parseInt(h.substring(2, 4), 16),
    b: parseInt(h.substring(4, 6), 16),
  };
}

/** Render a line of text onto ctx at (x, y). Supports per-character colors. */
function renderLine(ctx, text, x, y, font, color, shadow, shadowColor) {
  const charW = font.w + 1; // includes 1px kerning gap
  const isColorArray = Array.isArray(color);

  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    const glyph = getGlyph(font.glyphs, ch);
    const cx = x + i * charW;
    const charColor = isColorArray ? (color[i] || color[color.length - 1]) : color;

    // Shadow pass
    if (shadow) {
      ctx.fillStyle = shadowColor;
      drawGlyph(ctx, glyph, cx + 1, y + 1, font.w, font.h, shadowColor);
    }

    // Main pass
    ctx.fillStyle = charColor;
    drawGlyph(ctx, glyph, cx, y, font.w, font.h, charColor);
  }
}

