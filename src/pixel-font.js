export const PixelFont = {

  /**
   * Measure the width (and height) of rendered text in pixels.
   * Returns { width, height }.
   */
  measure(text, options) {
    const opts = defaults(options);
    const font = FONTS[opts.size] || FONTS.medium;
    const lh = opts.lineHeight || (font.h + 2);
    const lines = wordWrap(text, opts.maxWidth, font);

    let maxW = 0;
    for (let i = 0; i < lines.length; i++) {
      const w = measureLine(lines[i], font);
      if (w > maxW) maxW = w;
    }

    const totalH = lines.length * lh - (lh - font.h); // remove extra spacing after last line

    return {
      width: maxW + (opts.shadow ? 1 : 0),
      height: totalH + (opts.shadow ? 1 : 0),
    };
  },

  /**
   * Render text to an offscreen canvas and return it.
   * The canvas is tightly sized to the rendered text.
   */
  render(text, options) {
    const opts = defaults(options);
    const key = cacheKey(text, opts);
    const cached = cacheGet(key);
    if (cached) return cached;

    const font = FONTS[opts.size] || FONTS.medium;
    const lh = opts.lineHeight || (font.h + 2);
    const lines = wordWrap(text, opts.maxWidth, font);
    const { width, height } = this.measure(text, options);

    const canvas = createCanvas(Math.max(width, 1), Math.max(height, 1));
    const ctx = canvas.getContext('2d');

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      const lineW = measureLine(line, font);
      let lx = 0;
      if (opts.align === 'center') lx = Math.floor((width - lineW) / 2);
      else if (opts.align === 'right') lx = width - lineW;
      const ly = i * lh;
      renderLine(ctx, line, lx, ly, font, opts.color, opts.shadow, opts.shadowColor);
    }

    cacheSet(key, canvas);
    return canvas;
  },

  /**
   * Draw text directly onto an existing canvas context.
   * x, y is the top-left (adjusted by align).
   */
  draw(ctx, text, x, y, options) {
    const opts = defaults(options);
    const font = FONTS[opts.size] || FONTS.medium;
    const lh = opts.lineHeight || (font.h + 2);
    const lines = wordWrap(text, opts.maxWidth, font);

    // Compute total dimensions for alignment
    let maxW = 0;
    for (let i = 0; i < lines.length; i++) {
      const w = measureLine(lines[i], font);
      if (w > maxW) maxW = w;
    }

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      const lineW = measureLine(line, font);
      let lx = x;
      if (opts.align === 'center') lx = x + Math.floor((maxW - lineW) / 2);
      else if (opts.align === 'right') lx = x + maxW - lineW;
      const ly = y + i * lh;
      renderLine(ctx, line, lx, ly, font, opts.color, opts.shadow, opts.shadowColor);
    }
  },

  /**
   * Clear the render cache.
   */
  clearCache() {
    _cache.clear();
  },
};
